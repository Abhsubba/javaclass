package operator_PKG;

public class Multiple_Else {

	public static void main(String[] args) {
		
		
		int m = 0;
		
		if(m==0) {
			System.out.println("Nothing...");
		         }
		
		else if(m%2==0) 
		{
			
			System.out.println("Even...");
		}
		
		else {
			
			System.out.println("Odd..");
		}

	}

}
